    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        DefaultTableModel model = (DefaultTableModel) itemTable.getModel();
        PurchaseReturnDetails det = (PurchaseReturnDetails) cbMedicine.getSelectedItem();
        String submitQty = tfSubmit.getText();

        if (submitQty.trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Insert submit Quantity ???");
        } else if (det.getReturnQuantity() < Integer.parseInt(submitQty)) {
            JOptionPane.showMessageDialog(this, "Verify Submit Quantity !!!");
            tfSubmit.requestFocus();
        } else {
            if (!tempList.contains(det.getMedicine())) {
                model.addRow(new Object[]{det.getMedicine(), Integer.parseInt(submitQty)});
                tempList.add(det.getMedicine());
                det.setSubmitQuantity(det.getSubmitQuantity() + Integer.parseInt(submitQty));
                tempDetails.put(det.getMedicine().getMedicineId(), det);
            } else {
                JOptionPane.showMessageDialog(this, "Medicine Exists !!!");
            }
        }

    }//GEN-LAST:event_btnAddActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        DefaultTableModel model = (DefaultTableModel) itemTable.getModel();
        int itemsTaken = model.getRowCount();

        if (itemsTaken <= 0) {
            JOptionPane.showMessageDialog(this, "No Medicine Selected ???");
        } else {
            try {
                for (int i = 0; i < itemsTaken; i++) {
                    purchaseReturnTaken = new PurchaseReturnTaken();
                    Medicine medicine = (Medicine) model.getValueAt(i, 0);
                    int quantity = (int) model.getValueAt(i, 1);
                    PurchaseReturnDetails det = (PurchaseReturnDetails) tempDetails.get(medicine.getMedicineId());
                    purchaseReturnTaken.setReturnDetails(det);
                    purchaseReturnTaken.setQuantity(quantity);
                    purchaseReturnTaken.setUser(user);
                    purchaseReturnTaken.setTakenDate(chSubmitDate.getDate());
                    returnDao = new PurchaseReturnDao();
                    returnDao.returnTaken(purchaseReturnTaken, det);

                }
                model.setRowCount(0);
                cbReturnNo.removeAllItems();
                cbMedicine.removeAllItems();
                tfSubmit.setText("");
                tfToSubmit.setText("");

                JOptionPane.showMessageDialog(this, "Record saved successfully.");
            } catch (HeadlessException e) {
                System.out.println("Error..." + e);
            }
        }


    }//GEN-LAST:event_btnSaveActionPerformed

