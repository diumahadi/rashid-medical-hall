    private void cbRepresentativeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbRepresentativeActionPerformed

        try {
            if (cbRepresentative.getSelectedItem() != null) {
                Client c = (Client) cbRepresentative.getSelectedItem();
                masterDao = new PurchaseReturnMasterDao();
                cbReturnNo.removeAllItems();
                for (PurchaseReturnMaster mas : masterDao.getSingleRepReturn(c)) {
                    cbReturnNo.addItem(mas);
                }
                cbReturnNo.setSelectedIndex(-1);
            }

        } catch (Exception e) {
        }
    }//GEN-LAST:event_cbRepresentativeActionPerformed

    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed

        tempDetails = new HashMap<>();

        master = (PurchaseReturnMaster) cbReturnNo.getSelectedItem();
        detailsDao = new PurchaseReturnDeatilsDao();
        cbMedicine.removeAllItems();
        for (PurchaseReturnDetails det : detailsDao.childUnderParent(master)) {
            if (det.getReturnQuantity() != det.getSubmitQuantity()) {
                cbMedicine.addItem(det);
                tempDetails.put(det.getMedicine().getMedicineId(), det);
            }
        }

        if (tempDetails.isEmpty()) {
            JOptionPane.showMessageDialog(this, "All Returns are Taken.");
        }

        cbMedicine.setSelectedItem(null);
        tfSubmit.setText("");
        tfToSubmit.setText("");

    }//GEN-LAST:event_btnFindActionPerformed

